#include <iostream>
#include <cmath>


main()
{
    double x;
    // Addision
    x = 8 + 5;

    // Subtraction
    x = 8 - 5;

    // Multiplication
    x = 8 * 5;

    // Division
    x = 8 / 5;

    // Exponents, 8^5
    x = pow(8, 5);

    // Square root
    x = sqrt(x);

    // Absolute value
    x = abs(x);

    // Rounded value
    x = 7.8;
    x = round(x);

    // Round up and down
    x = floor(x);
    x = ceil(x);

    return 0;
}